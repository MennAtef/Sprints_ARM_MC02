/******************************************************************************/
void SysTickInit(float32 time)
{
	u32 Reload = ( time / (1/16000000)) - 1;
    SYSTICK_REGISTER->STCTRL.R|=(SYSTICK_COUNTER)|(SYSTICK_INTERRUPT<<1)|(SYSTICK_CLKSRC<<2);
    SYSTICK_REGISTER->STRELOAD.R=Reload;
}

