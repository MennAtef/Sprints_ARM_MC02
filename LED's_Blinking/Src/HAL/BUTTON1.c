void BUTTON1_Init(void)
{
    void DIO_SetPinDir (BUTTON1_PORT, BUTTON1_PIN , BUTTON1_INP);
    SET_BIT(PORTC_PUR , BUTTON1_PIN);
}
uint8 BUTTON_Read(void)
{
    uint8 BTN = NOT_PRESSED;
    uint8 state = 1;
    state = DIO_ReadPinVal (BUTTON1_PORT, BUTTON1_PIN);
    BTN = state;
    while (state != 1)
    {
        state = DIO_ReadPinVal (BUTTON1_PORT, BUTTON1_PIN);
    }
    return BTN;
}
