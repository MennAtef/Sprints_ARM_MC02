void LED_Init(void)
{
    void DIO_SetPinDir (LED_PORT, LED_PIN , LED_STATE);
}
 
void LED_ON(void)
{
    DIO_SetPinVal (LED_PORT , LED_PIN , LED_ON);
}
void LED_OFF(void)
{
    DIO_SetPinVal (LED_PORT, LED_PIN, LED_OFF);
}
void LED_TOG(void)
{
    DIO_TogglePinValue(LED_PORT,LED_PIN);
}

