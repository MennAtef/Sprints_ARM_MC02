void BUTTON2_Init(void)
{
    void DIO_SetPinDir (BUTTON2_PORT, BUTTON2_PIN , BUTTON2_INP);
    SET_BIT(PORTC_PUR , BUTTON2_PIN);
}
uint8 BUTTON_Read(void)
{
    uint8 BTN = NOT_PRESSED;
    uint8 state = 1;
    state = DIO_ReadPinVal (BUTTON2_PORT, BUTTON2_PIN);
    BTN = state;
    while (state != 1)
    {
        state = DIO_ReadPinVal (BUTTON2_PORT, BUTTON2_PIN);
    }
    return BTN;
}
