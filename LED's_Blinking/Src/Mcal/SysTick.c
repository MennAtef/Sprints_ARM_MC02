void SysTick_Init(uint8 power_on_duty_cycle)
{
    if(INT_STATUS == INT_ENABLE)
    {
        NVIC_ST_CTRL |= (1<<1) ;
    }
    else
    {
       NVIC_ST_CTRL &=~(1<<1) ; 
    }

    if(CLK_SOURCE == INTRERNAL_CLK)
    {
        NVIC_ST_CTRL |= (1<<2) ;
    }
    else
    {
       NVIC_ST_CTRL &=~(1<<2) ; 
    }

    NVIC_ST_CURRENT_R = 0;
	if(power_on_duty_cycle>0 && power_on_duty_cycle<100)
    {
		NVIC_ST_RELOAD_R=(16000000-((*duty_cycle)*16000000/100))-1;
	}
}
